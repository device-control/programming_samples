#include "Locker.h"

////////////////////////////////////////////////////////////////////////////////
Locker::Locker(HANDLE &mutex)
	: m_mutex(mutex)
{
	lock();
}

////////////////////////////////////////////////////////////////////////////////
Locker::~Locker()
{
	unlock();
}

////////////////////////////////////////////////////////////////////////////////
void Locker::lock()
{
	WaitForSingleObject(m_mutex, INFINITE);
}

////////////////////////////////////////////////////////////////////////////////
void Locker::unlock()
{
	ReleaseMutex(m_mutex);
}

#include "Event.h"

////////////////////////////////////////////////////////////////////////////////
Event::Event()
{
}

///////////////////////////////////////////////////////////////////////////////
Event::Event(const std::string& name, int sequenceNo/*=0*/)
	: m_name(name)
	, m_entity("")
	, m_sequenceNo(sequenceNo)
{
}

///////////////////////////////////////////////////////////////////////////////
Event::Event(const std::string& name, const std::string& entity, int sequenceNo/*=0*/)
	: m_name(name)
	, m_entity(entity)
	, m_sequenceNo(sequenceNo)
{
}

////////////////////////////////////////////////////////////////////////////////
Event::~Event()
{
}

////////////////////////////////////////////////////////////////////////////////
void Event::setName(const std::string& name)
{
	m_name = name;
}

////////////////////////////////////////////////////////////////////////////////
const std::string& Event::getName() const
{
    return m_name;
}

////////////////////////////////////////////////////////////////////////////////
void Event::setSequenceNo(int sequenceNo)
{
	m_sequenceNo = sequenceNo;
}

////////////////////////////////////////////////////////////////////////////////
short Event::getSequenceNo() const
{
    return m_sequenceNo;
}

////////////////////////////////////////////////////////////////////////////////
void Event::setEntity(const std::string& entity)
{
	m_entity = entity;
}

////////////////////////////////////////////////////////////////////////////////
const std::string& Event::getEntity() const
{
    return m_entity;
}

